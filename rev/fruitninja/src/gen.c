__attribute__((naked)) int hcall(int sysno, ...) {
  asm volatile("nop\n"
               "nop\n"
               "nop\n"
               "nop\n"
               "nop\n"
               "lui t0, 0x2000\n"
               "lw  a0, 0x14(t0)\n"
               "ret\n" ::
                   : "memory");
}

__attribute__((naked, noreturn)) void exit(int code) {
  asm volatile("nop\n"
               "nop\n"
               "nop\n"
               "nop\n"
               "nop\n"
               "lui t0, 0x2000\n"
               "sw  zero, 0x10(t0)\n");
}

